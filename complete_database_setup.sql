-- 1. PROFILES TABLE
CREATE TABLE public.profiles (
  id uuid REFERENCES auth.users NOT NULL PRIMARY KEY,
  full_name text,
  role user_role DEFAULT 'buyer',
  agency_name text,
  rera_number text,
  is_verified boolean DEFAULT false,
  phone text,
  experience_years integer,
  specialization text,
  office_address text,
  document_urls text[]
);

-- 2. PROPERTIES TABLE
CREATE TABLE public.properties (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  title text NOT NULL,
  description text,
  price numeric,
  location text,
  pincode text,
  unit_number text,
  area_sqft numeric,
  bedrooms int,
  bathrooms int,
  type text,
  images text[],
  video_url text,
  status property_status DEFAULT 'pending',
  posted_by uuid REFERENCES public.profiles(id) NOT NULL,
  latitude float,
  longitude float,
  furnishing_status furnishing_type DEFAULT 'unfurnished',
  property_age property_age_type DEFAULT 'new',
  floor_number integer,
  total_floors integer,
  year_built integer,
  amenities text[],
  ownership_docs text[],
  ai_generated_description boolean DEFAULT false,
  commute_info jsonb
);

-- 3. INQUIRIES TABLE
CREATE TABLE public.inquiries (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  property_id bigint REFERENCES public.properties(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  name text NOT NULL,
  email text NOT NULL,
  message text,
  status text DEFAULT 'new' CHECK (status IN ('new', 'replied', 'closed')),
  response text,
  responded_at timestamp with time zone,
  responded_by uuid REFERENCES public.profiles(id),
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 4. NOTIFICATIONS TABLE
CREATE TABLE public.notifications (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  user_id uuid REFERENCES public.profiles(id) NOT NULL,
  message text,
  is_read boolean DEFAULT false
);

-- 5. SAVED PROPERTIES TABLE
CREATE TABLE public.saved_properties (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  property_id bigint REFERENCES public.properties(id) ON DELETE CASCADE NOT NULL,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(user_id, property_id)
);

-- 6. PROPERTY ALERTS TABLE (AI Features)
CREATE TABLE public.property_alerts (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
  preferences jsonb NOT NULL,
  enabled boolean DEFAULT true,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 7. AI USAGE TRACKING TABLE
CREATE TABLE public.ai_usage (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  feature text NOT NULL,
  tokens_used integer,
  cost numeric(10, 6),
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);


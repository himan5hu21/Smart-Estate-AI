CREATE POLICY "Public profiles are viewable by everyone" 
  ON public.profiles FOR SELECT 
  USING (true);

CREATE POLICY "Users can update own profile OR Admin can update any" 
  ON public.profiles FOR UPDATE 
  USING (auth.uid() = id OR public.is_admin() = true);

CREATE POLICY "Only Admin can delete profiles" 
  ON public.profiles FOR DELETE 
  USING (public.is_admin() = true);

-- ===================================================
-- PROPERTIES POLICIES
-- ===================================================

CREATE POLICY "Read access for properties" 
  ON public.properties FOR SELECT 
  USING (
    status = 'active' OR 
    auth.uid() = posted_by OR 
    public.is_admin() = true
  );

CREATE POLICY "Users can insert their own properties" 
  ON public.properties FOR INSERT 
  WITH CHECK (auth.uid() = posted_by);

CREATE POLICY "Owners and Admins can update properties" 
  ON public.properties FOR UPDATE 
  USING (
    auth.uid() = posted_by OR 
    public.is_admin() = true
  );

CREATE POLICY "Owners and Admins can delete properties" 
  ON public.properties FOR DELETE 
  USING (
    auth.uid() = posted_by OR 
    public.is_admin() = true
  );

-- ===================================================
-- INQUIRIES POLICIES
-- ===================================================

-- Anyone can submit inquiries
CREATE POLICY "Anyone can insert inquiries" 
  ON public.inquiries FOR INSERT 
  WITH CHECK (true);

-- Property owners, admins, and inquiry submitters can view inquiries
CREATE POLICY "Property owners can view inquiries" 
  ON public.inquiries FOR SELECT 
  USING (
    auth.uid() IN (
      SELECT posted_by FROM public.properties WHERE id = property_id
    ) OR 
    public.is_admin() = true OR
    auth.uid() = user_id
  );

-- Property owners and admins can update inquiries (to respond)
CREATE POLICY "Property owners can update inquiries" 
  ON public.inquiries FOR UPDATE 
  USING (
    auth.uid() IN (
      SELECT posted_by FROM public.properties WHERE id = property_id
    ) OR 
    public.is_admin() = true
  );

